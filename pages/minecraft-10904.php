<span class="kbsectionheader">Установка:</span><br>
1. Открываем папку <i>.minecraft</i> (на Windows: нажимаем "Пуск", там вводим в поле поиска или "выполнить" слово <i>%appdata%</i>, и перед нами сразу же откроется папка, содержащая папку <i>.minecraft</i><br>
(для Win7, 8 и 10 это C:\Пользователи\%username%\AppData\Roaming\).<br>
2. Копируем скачанный архив в папку <i>resourcepacks</i>.<br>
3. Запускаем Minecraft, активируем ресурспак "RUS_194".<br>
(4.) Выбираем язык "Русский (KB)", если используем вариант с отдельным .lang-файлом (первая ссылка).<br>
(5.) Если русификатор не работает, попробуйте поднимать его выше в списке выбранных ресурспаков, пока не заработает.<br>
