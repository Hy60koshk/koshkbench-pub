<p><span class="kbsectionheader">Установка на Java Edition:</span></p>
<p>1. Копируем скачанный файл в папку с пакетами ресурсов. Найти эту папку можно из самой игры, перейдя в настройки -> Наборы ресурсов (Resource Packs), и там нажать «Папка с наборами» (Open Pack Folder).<br>
2. Затем в игре активируем пакет ресурсов «RUS_1_20_1».<br>
3. Если устанавливаем как отдельный язык, то выбираем язык «Русский (KB)». Если как замену оригинального, то «Русский (Россия)».<br>
(4.) Если перевод не работает или вдруг перестал работать, попробуйте поднять его выше в списке выбранных ресурспаков, пока не заработает.
</p>
<br>
<p><span class="kbsectionheader">Установка на Bedrock Edition:</span></p>
<p>1. Cкачиваем .mcpack<br>
2. Открываем файл, собственно, Майнкрафтом.<br>
3. Заходим в настройки и активируем набор ресурсов «Перевод 1.20 - KoshkBench».<br>
(4.) Если шрифт выглядит как-то криво, то перезапустите игру.<br>
(5.) Если изначально язык стоял не русский, то выберите его в настройках языка.<br>
(6.) Если у вас стоят пакеты ресурсов, изменяющие стандартные предметы на какие-то другие (напрмер, мебель), то такие пакеты ресурсов должны иметь более высокий приоритет загрузки в списке пакетов, чем мой перевод.<br>
(7.) Если ничего не работает - ругаемся мне через кнопочку «Написать автору» внизу этой страницы или в комментариях к статье.
