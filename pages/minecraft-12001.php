<div class="kbspoilerheader kbsectionheader" for="settingsSpoiler">Краткий гайд по настройкам графики</div>
<div class="kbspoiler" id="settingsSpoiler">
Я понимаю, что в настройках графики можно заблудиться, да и кто-то мог уже привыкнуть к старым названиям. Плюс, кто-то может узнать что-то новое. Поэтому расписываю ниже, что за что отвечает. На всякий случай.<br>
<ul>
<li><b>Полноэкранное разрешение</b> — разрешение, действующее при игре во весь экран (F11).</li>
<li><b>Граница биомов</b> — ширина (в блоках) полосы на границе двух биомов, в которой цвет травы и листьев будет смешиваться для более плавного перехода между биомами.</li>
<li><b>Графика</b> — древнейший и первозданно отвратительный переключатель, отвечающий сразу за несколько параметров, самый заметный из которых — прозрачность листьев.</li>
<li><b>Видимость</b> — радиус видимости в чанках. Напрямую влияет на лаги и объём потребляемой оперативной памяти.</li>
<li><b>Радиус процессов</b> — радиус в чанках вокруг игрока, в котором будут выполняться различные расчёты, например, для передвижения мобов, работы механизмов и роста растений. Позволяет снизить лаги при большом радиусе видимости, но создаст эффект «мира на паузе» для отдалённых объектов.</li>
<li><b>Обновление чанков</b> — отвечает за то, будут ли ближайшие к игроку чанки обновляться синхронно (в том же потоке, в котором над ними были произведены изменения).
«Асинхронное» — ближайшие к игроку чанки обновляются в параллельных потоках. Из-за этого после разрушения блока на его месте на короткое время может возникать дыра.
«Полусинхронное» — некоторые действия, в т.ч. разрушение блоков, запустят синхронное обновление изменённого чанка.
«Синхронное» — ближайшие к игроку чанки всегда обновляются синхронно. Это может вызывать притормаживания игры при разрушении/размещении блоков.</li>
<li><b>Сглаженные тени</b> — градиентное сглаживание теней от блока к блоку. Выкл — каждый блок целиком будет иметь свою собственную яркость, без градиентного перехода.</li>
<li><b>Ограничение FPS</b> — ограничение количества кадров. Убирает излишнюю нагрузку на систему, особенно полезно для легко перегревающихся систем (напрмер, ноутбуков).</li>
<li><b>Вертикальная синхронизация</b> — форсирование ожидания подготовки кадра целиком перед его отображением. Убирает «разрывы» изображения, но даже на мощных ПК может создавать излишние лаги в нагруженных эффектами кадрах и вызывать почти постоянный эффект вязкости мыши. Ограничивает максимальное кол-во кадров до частоты обновления вашего монитора.</li>
<li><b>Покачивание камеры</b> — покачивание камеры при ходьбе. На самом деле, практически не влияет на производительность, но по ощущениям делает микротормоза в разы заметнее из-за перескоков камеры.</li>
<li><b>Размер интерфейса</b> — размер интерфейса.</li>
<li><b>Индикатор атаки</b> — расположение индикатора атаки: рядом с прицелом в центре экрана или внизу рядом с индикаторами здоровья.</li>
<li><b>Светлость блоков</b> — параметр, который часто путают с яркостью. Отвечает за дополнительную освещённость блоков сверх обычного. 0 — без дополнительной подсветки, исторически задуманный. До 50 — комфортные значения для более светлого ощущения мира. От 50 до 100 — пересвет, с которым яркость хорошо освещённых блоков не соответствует своему реальному уровню яркости, такие значения полезны при игре с низкой яркостью экрана или при исследовании пещер без освещения.</li>
<li><b>Облака</b> — без облаков, плоская текстура облаков, или 3D-модель облаков.</li>
<li><b>Полноэкранный режим</b> — переключение полноэкранного режима (F11).</li>
<li><b>Частицы</b> — количество отображаемых частиц. При настройке «минимум» не будет отображаться, например, дым от факела.</li>
<li><b>Число MIP-уровней</b> — количество уровней MIP-маппинга, т.е. дополнительных текстур меньшего разрешения, которые используются на больших расстояниях для ликвидации ряби. Влияние на производительность сильно зависит от системы.</li>
<li><b>Тени сущностей</b> — круглые тени под мобами, игроками и выпадающими предметами.</li>
<li><b>Дальность сущностей</b> — дальность прорисовки сущностей (мобов, игроков, предметов).</li>
<li><b>Эффекты искажения</b> — эффекты искажения экрана в порталах, эффект тошноты.</li>
<li><b>Эффекты поля зрения</b> — эффект бега.</li>
</div>
