<br><br>
<span class="kbsectionheader">Пакет включает в себя:</span><br>
- перевод меню и названий всех предметов до версии 1.17.1 включительно (полностью совместим с 1.17);<br>
- перевод финального диалога и splash'ей.<br>
<br>
<span class="kbsectionheader">Заметные изменения:</span><br>
- альтернативный перевод всего адского (начиная с того, что Nether здесь - Преисподняя);<br>
- альтернативный перевод нескольких видов мобов (в т.ч. вернулись свинолюди и свинозомби);<br>
- для упрощения поиска и сортировки, теперь всё "из дерева", т.е. вместо "дубовой кнопки" будет "кнопка из дуба";<br>
- включает исправления для некоторых модов.<br>
