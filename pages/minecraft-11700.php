<span class="kbsectionheader">Установка:</span><br>
1. Копируем скачанный файл в папку с пакетами ресурсов. Найти эту папку можно из самой игры, перейдя в настройки -> Наборы ресурсов (Resource Packs), и там нажать "Папка с наборами"/"Open Pack Folder".<br>
2. Затем в игре активируем пакет ресурсов "RUS_1_17_1".<br>
3. Выбираем язык "Русский KB".<br>
(4.) Если русификатор не работает или вдруг перестал работать, попробуйте поднять его выше в списке выбранных ресурспаков, пока не заработает.<br>
