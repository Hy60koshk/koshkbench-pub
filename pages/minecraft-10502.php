<p><b><span style="font-size:12pt;">Установка:</span></b></p>
<p><b>Клиент:</b></p>
<p>1. Открываем папку <kbfolder>%AppData%</kbfolder> («Пуск», там вводим в поле поиска или «выполнить» слово <kbcode>%appdata%</kbcode>, и перед нами сразу же откроется папка, содержащая папку <kbfolder>.minecraft</kbfolder><br>
(для Win7 это <kbfolder>C:\Пользователи\%username%\AppData\Roaming\</kbfolder>).</p>
<p>2. Открываем папку <kbfolder>bin</kbfolder>. Копируем в нее файлы <kbcode>jinput.jar</kbcode>, <kbcode>lwjgl.jar</kbcode>, <kbcode>lwjgl_util.jar</kbcode> и папку <kbfolder>natives</kbfolder>, которые лежат в папке <kbfolder>lwjgl</kbfolder> русификатора.</p>
<p>3. В той же папке <kbfolder>bin</kbfolder> находим <kbcode>minecraft.jar</kbcode>, открываем его через Winrar (или аналог, например 7zip).</p>
<p>4. Удаляем из него папку <kbfolder>META-INF</kbfolder>.</p>
<p>5. Если нужно произвести полную установку данного русификатора, копируем все из папки <kbfolder>Russifier</kbfolder>.<br>
Если нужно сохранить английский язык интерфейса, не копируем папки <kbfolder>lang</kbfolder> и <kbfolder>title</kbfolder>.</p>
<p>6. Внутри игры выбираем английский язык (который «English (US)»).</p>
<br>
<p><b>Сервер:</b></p>
<p>Открываем через Winrar или 7zip ваш <kbcode>minecraft_server.jar</kbcode>.</p>
<p>Чтобы писать по-русски на табличках, копируем из папки <kbfolder>Russifier</kbfolder> файл <kbcode>font.txt</kbcode> в серверный .jar.</p>
<p>Чтобы игроки видели серверные надписи из данного русификатора, а не стандартные, из папки <kbfolder>Russifier</kbfolder> копируем папку <kbfolder>lang</kbfolder> в серверный .jar.</p>
