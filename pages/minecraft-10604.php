</p><p>3. Если у вас современный Minecraft Launcher: Переходите на вкладку «Установки», там вверху включаете галочку «Модифицированные», затем создаёте новую «установку», и в поле «версия» выбираете <kbcode>1.6.4_ru_KB</kbcode>.<br>
