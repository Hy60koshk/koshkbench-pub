<p>1. Открываем папку <kbfolder>.minecraft</kbfolder> ("Пуск", там вводим в поле поиска или "выполнить" слово <kbcode>%appdata%</kbcode>, и перед нами сразу же откроется папка, содержащая папку <kbfolder>.minecraft</kbfolder><br>
