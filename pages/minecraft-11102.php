<p>Предупреждение: книги в модах могут быть переведены постранично, из-за чего при достаточно широком шрифте (касается в т.ч. "классического" шрифта) текст может вылезать за нижнюю границу книжной страницы.</p>

<span class="kbsectionheader">Установка:</span><br>
1. Копируем скачанный файл в папку с пакетами ресурсов (папка <i>resourcepacks</i> в папке с игрой).<br>
2. Запускаем Minecraft, активируем ресурспак "RUS_1112_rureplacer".<br>
3. Выбираем русский язык.<br>
(4.) Если русификатор не работает или вдруг перестал работать, попробуйте поднимать его выше в списке выбранных ресурспаков, пока не заработает.<br>
