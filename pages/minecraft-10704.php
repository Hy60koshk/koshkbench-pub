<h3 class='post-title entry-title'>Старый русификатор Minecraft 1.7.4,<br>он же «обновлённый», он же «rev2»</h3>

<div class='post-body entry-content'>

<img src="/koshkbench/pics/rus_1_7_4/logo.png" class="kbImage">
<br><br>
<b>Для версии 1.7.5 также используйте этот русификатор.</b><br><br>

<span style="font-size:12pt;"><b>Русификатор включает в себя:</b></span><br>
- полностью переписанный перевод меню и названий всех предметов по 1.7.4 включительно;<br>
- обновленный перевод финального диалога и титров;<br>
- заполнение некоторых пропущенных названий предметов (которых нет даже на англ.);<br>
- перевод splash'ей (часть сохранена на английском или других языках, т.к. не имеют смысла после перевода, например, приветствия на разных языках или строчки песен);<br>
- классический шрифт в меню.<br>
<br>

<img src="/koshkbench/pics/rus_1_7_4/sett1.png" class="kbImage" width="500px" id="settimg">
<br><br>
<b><span style="font-size:12pt;">Варианты перевода:</span></b><br>
Пакет ресурсов с классическим шрифтом добавляет языки:<br>
"Русский обновленный (KB)" — обновленный перевод, классический шрифт;<br>
"Русский (KB)" — старый перевод, классический шрифт.<br>
* Если у вас уже выбран русский язык, то мои варианты языка будут выглядеть как "®yccôìÄ".<br>
<br>
Пакет с юникодовым шрифтом добавляет только "Русский обновленный (KB)" для стандартного тонкого шрифта.<br><br>

