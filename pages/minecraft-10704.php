<b><span style="font-size:12pt;">Установка:</span></b><br>
1) открываем папку <i>.minecraft</i> ("Пуск", там вводим в поле поиска или "выполнить" слово <i>%appdata%</i>, и перед нами сразу же откроется папка, содержащая папку <i>.minecraft</i><br>
(для Win7 это C:\Пользователи\%username%\AppData\Roaming\);<br>
2) копируем скачанный архив в папку <i>resourcepacks</i>;<br>
3) запускаем Minecraft, выбираем ресурспак "RUS_174_classic_rev2.1" или "RUS_174_uni_rev2";<br>
4) выбираем язык в соответствии с пунктом <b>Варианты перевода</b>.<br>
* Если вы используете несколько ресурспаков и русификатор не работает, убедитесь, что русификатор грузится последним (в самом верху списка).<br>
