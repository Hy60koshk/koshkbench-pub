<b><span style="font-size:12pt;">Установка (ресурспак):</span></b><br>
1. Открываем папку <i>.minecraft</i> (на Windows: нажимаем "Пуск", там вводим в поле поиска или "выполнить" слово <i>%appdata%</i>, и перед нами сразу же откроется папка, содержащая папку <i>.minecraft</i><br>
(для Win7 и 8 это C:\Пользователи\%username%\AppData\Roaming\).<br>
2. Копируем скачанный архив в папку <i>resourcepacks</i>.<br>
3. Запускаем Minecraft, выбираем ресурспак "RUS_1710_rev7".<br>
4. Выбираем язык "Русский (KB)".<br>
5. Перезапускаем Minecraft.<br>
(6.) Если вы используете несколько ресурспаков, убедитесь, что русификатор грузится последним (в самом верху списка).<br><br>

<b><span style="font-size:12pt;">Установка (модификатор jar-а):</span></b><br>
1. Открываем папку <i>.minecraft</i> (на Windows: нажимаем "Пуск", там вводим в поле поиска или "выполнить" слово <i>%appdata%</i>, и перед нами сразу же откроется папка, содержащая папку <i>.minecraft</i><br>
(для Win7 и 8 это C:\Пользователи\%username%\AppData\Roaming\).<br>
2. Заходим в папку <i>versions</i>, там должна быть папка <i>1.7.10</i> (если ее нет - запустите лаунчер и скачайте версию 1.7.10).<br>
3. Делаем копию папки <i>1.7.10</i>, переименовываем эту копию, например, в <i>1.7.10rus</i>. Все файлы в этой папке (jar и json) переименовываем так же.<br>
4. Открываем файл <i>1.7.10rus.json</i>, в нем заменяем <span class=kbcode>"id": "1.7.10"</span> на<br><span class=kbcode>"id": "1.7.10_RU"</span>. Не потеряйте в конце запятую ;)<br>
5. Открываем <i>1.7.10rus.jar</i> любым архиватором (7zip, winrar), в нем удаляем папку META-INF (варварский, но простой способ; более сложный указан <a href="http://only-minecraft.ru/mody-minecraft/15-1710-rusifikator-minecraft.html">здесь</a>) и копируем в него все файлы из скачанного отсюда архива.<br>
6. Играем (если лаунчер был запущен, его надо перезапустить).<br><br>

Измененный класс отрисовщика шрифта взят из <a href="http://only-minecraft.ru/mody-minecraft/15-1710-rusifikator-minecraft.html">русификатора с only-minecraft.ru</a>.<br><br>
