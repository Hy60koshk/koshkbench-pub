<b><span style="font-size:12pt;">Установка:</span></b><br>
1. Открываем папку <i>.minecraft</i> (на Windows: нажимаем "Пуск", там вводим в поле поиска или "выполнить" слово <i>%appdata%</i>, и перед нами сразу же откроется папка, содержащая папку <i>.minecraft</i><br>
(для Win7, 8 и 10 это C:\Пользователи\%username%\AppData\Roaming\).<br>
2. Копируем скачанный архив в папку <i>resourcepacks</i>.<br>
3. Запускаем Minecraft, выбираем ресурспак "RUS_189_uni".<br>
4. Выбираем язык "Русский (KB)".<br>
(5.) Если не работает и вы используете несколько ресурспаков, убедитесь, что русификатор грузится последним (в самом верху списка).
<br><br>
Для восстановления классического шрифта используйте <a href="https://optifine.net/home">OptiFine</a> с одним из <a href="https://koshk.ru/koshkbench/?page=mc-font-109">этих шрифтов</a>, или <a href="https://github.com/gamerforEA/Minecraft-ClientFixer/releases/">ClienFixer</a> (мод для Forge).<br>
Если хотите заменить шрифт на более читаемый без установки модов, можно использовать, например, <a href="https://koshk.ru/koshkbench/?page=mc-font-108">этот шрифт</a>, или найти другой по своему вкусу в интернете.<br>
В версии Minecraft 1.9 баги с изменением шрифта поправлены и необходимость в OptiFine/ClientFixer'e для достижения данной цели отпадает (начиная с S16w02).<br><br>
